package com.penan.project_spring_ioc;

public class LoanAccount {

    int id;
    String name;

    public LoanAccount() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
